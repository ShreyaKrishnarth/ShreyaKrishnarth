def detect_outliers_iqr(filtered_data):
    outliers_dict = {}
    for column in filtered_data.select_dtypes(include=['float64', 'int64']).columns:
        Q1 = filtered_data[column].quantile(0.25)
        Q3 = filtered_data[column].quantile(0.75)
